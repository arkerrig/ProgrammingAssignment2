## Pair of functions to check for inverse of matrix in cache, compute if 
## necessary and return inverse of matrix x

## makeCacheMatrix creates 'matrix' object that can cache its invers
## cacheSolve checks for inverse of matrix x in cache and returns if available
## If inverse not in cache, cacheSolve calculates and stores then return inverse

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) m <<- solve
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


